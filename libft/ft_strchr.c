#include "libft.h"

char	*ft_strchr(char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] || !c)
	{
		if (s[i] == (unsigned char)c)
			return (&(s[i]));
		i++;
	}
	return (0x0);
}
